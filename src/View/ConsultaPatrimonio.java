    private void campo_patrimonioKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_campo_patrimonioKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER && campo_patrimonio.getText().isEmpty()) {
            campo_marca.requestFocus();
            readTable();
        } else if (evt.getKeyCode() == KeyEvent.VK_ENTER && !campo_patrimonio.getText().isEmpty()) {
            readForTombamento(campo_patrimonio.getText());
            campo_patrimonio.selectAll();
        } else if (evt.getKeyCode() == KeyEvent.VK_DELETE && campo_patrimonio.getText().isEmpty()) {
            readTable();
        } else if (evt.getKeyCode() == KeyEvent.VK_DELETE) {
            readTable();
        }
    }//GEN-LAST:event_campo_patrimonioKeyPressed

