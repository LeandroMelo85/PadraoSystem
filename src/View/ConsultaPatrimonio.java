    public static void readTable() {
        DefaultTableModel modelo = (DefaultTableModel) tabelaPatrimonio.getModel();
        modelo.setNumRows(0);

        CadastroDAO.readPatrimonio().forEach((pat) -> {
            modelo.addRow(new Object[]{
                pat.getNumTombamento(),
                pat.getDescricao(),
                pat.getMarca(),
                pat.getObservacao(),
                pat.getSetor().getSetorNome(),
                pat.getComputador().getProcessador(),
                pat.getComputador().getMemoria(),
                pat.getComputador().getHd()
            });
        });
    }

